    private void setDataTotalIncomePay() {
        double total = 0;
        double income = 0;
        double pay = 0;
        for (Budget budget : budgets) {
            if (budget.getStatus()) {
                income += budget.getValues();
            } else {
                pay += budget.getValues();
            }
        }
        total = income - pay;
        txt_total.setText(total + "");
        txt_income.setText(income + "");
        txt_pay.setText(pay + "");
    }

    private void initOrUpdateData() {
        getDataFromDB();
        bindDataIntTable(budgets);
        setDataTotalIncomePay();
    }

    private void setDataInComboBox() {
        int month = LocalDate.now().getMonthValue();
        int year = LocalDate.now().getYear();
        cbx_month.setSelectedItem(month + "");
        cbx_year.setSelectedItem(year + "");
    }

    private void getDataFromDB() {
        int month = Integer.parseInt((String) cbx_month.getSelectedItem());
        int year = Integer.parseInt((String) cbx_year.getSelectedItem());
        budgets = bgDao.selectBudgetAll(currentUser, month, year);
    }

    private void bindDataIntTable(List<Budget> lst) {
        bgInTable = lst;
        String[] header = {"Id", "Title", "Create day", "Value", "Status"};
        DefaultTableModel model = new DefaultTableModel(null, header);
        for (Budget budget : lst) {
            Vector v = new Vector();
            v.add(budget.getId());
            v.add(budget.getTitle());
            v.add(budget.getCreateDate());
            v.add(budget.getValues());
            v.add(budget.getStatus() ? "Income" : "Pay");
            model.addRow(v);
        }
        modelTable.setModel(model);

    }

