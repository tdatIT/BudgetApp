    private void cbx_typeItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cbx_typeItemStateChanged
        // TODO add your handling code here:
        List<Budget> filterBg = new ArrayList<Budget>();
        String type_name = String.valueOf(cbx_type.getSelectedItem());
        if (!type_name.equals("ALL")) {
            boolean type = type_name.equals("INCOME");
            for (Budget budget : budgets) {
                if (budget.getStatus() == type) {
                    filterBg.add(budget);
                }
            }
            bindDataIntTable(filterBg);
        } else
            bindDataIntTable(budgets);
    }//GEN-LAST:event_cbx_typeItemStateChanged

    private void cbx_sortItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cbx_sortItemStateChanged
        // TODO add your handling code here:
        List<Budget> sortBg = bgInTable;
        String sort_request = String.valueOf(cbx_sort.getSelectedItem());
        if (sort_request.equals("L -> H")) {
            Collections.sort(sortBg);
        } else {
            Collections.sort(sortBg, Collections.reverseOrder());
        }
        bindDataIntTable(sortBg);

    }//GEN-LAST:event_cbx_sortItemStateChanged

