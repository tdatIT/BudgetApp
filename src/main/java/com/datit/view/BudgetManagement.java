    private void btn_delActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_delActionPerformed
        // TODO add your handling code here:
        if (checkedIdSelected()) {
            long idSelected = Long.parseLong(txt_idSelected.getText());
            int reply = JOptionPane.showConfirmDialog(null,
                    "Do you what delete title have Id [" + idSelected + "] ?",
                    "Delete Confirm",
                    JOptionPane.YES_NO_OPTION);
            if (reply == JOptionPane.YES_OPTION) {
                if (bgDao.deleteBudget(idSelected)) {
                    JOptionPane.showMessageDialog(null, "Delete Success", "Notification",
                            JOptionPane.INFORMATION_MESSAGE);
                } else {
                    JOptionPane.showMessageDialog(null, "Delete Fail", "Notification",
                            JOptionPane.ERROR_MESSAGE);
                }
            }
        }
    }//GEN-LAST:event_btn_delActionPerformed

